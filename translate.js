let currentLang = 'en';

function applyTranslation(text) {
  let mapped = text;
  // First match exactly
  if (translations[text]) return translations[text];
  
  // Replace substrings cleanly
  for (const [en, ge] of Object.entries(translations)) {
      if (mapped.includes(en)) {
          mapped = mapped.split(en).join(ge);
      }
  }
  return mapped;
}

function translateNode(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    let text = node.nodeValue;
    if (!text.trim()) return;
    
    if (typeof node._originalText === 'undefined') {
      node._originalText = text;
    }
    
    if (currentLang === 'ge') {
      node.nodeValue = applyTranslation(node._originalText);
    } else {
      node.nodeValue = node._originalText;
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE' && node.tagName !== 'svg') {
      node.childNodes.forEach(child => translateNode(child));
    }
  }
}

function retranslate() {
  document.body.classList.toggle('lang-ge', currentLang === 'ge');
  
  // Translate document title
  let ogTitle = document._originalTitle;
  if (!ogTitle) {
      ogTitle = document.title;
      document._originalTitle = ogTitle;
  }
  document.title = currentLang === 'ge' ? applyTranslation(ogTitle) : ogTitle;

  document.querySelectorAll('body, body *').forEach(n => {
     // Translate inputs
     if (n.tagName === 'INPUT' || n.tagName === 'TEXTAREA') {
         if (n.placeholder) {
             if (!n._originalPlaceholder) n._originalPlaceholder = n.placeholder;
             n.placeholder = currentLang === 'ge' ? applyTranslation(n._originalPlaceholder) : n._originalPlaceholder;
         }
         if (n.value && n.type && n.type.toLowerCase() === 'submit') { // only submit buttons value
             if (!n._originalValue) n._originalValue = n.value;
             n.value = currentLang === 'ge' ? applyTranslation(n._originalValue) : n._originalValue;
         }
     }
     n.childNodes.forEach(c => {
         if(c.nodeType === Node.TEXT_NODE) translateNode(c);
     });
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const toggleBtns = document.querySelectorAll('.lang-toggle-btn');
  toggleBtns.forEach(btn => {
    btn.addEventListener('click', function(e) {
      e.preventDefault();
      currentLang = currentLang === 'en' ? 'ge' : 'en';
      
      const newImgSrc = currentLang === 'en' ? 'https://flagcdn.com/ge.svg' : 'https://flagcdn.com/us.svg';
      const newImgAlt = currentLang === 'en' ? 'Georgian Flag' : 'US Flag';
      const newTitle = currentLang === 'en' ? 'Switch to Georgian' : 'Switch to English';

      document.querySelectorAll('.lang-toggle-btn').forEach(b => {
          b.title = newTitle;
          const img = b.querySelector('img');
          if (img) {
              img.src = newImgSrc;
              img.alt = newImgAlt;
          }
      });
      
      retranslate();
    });
  });
  
let observer = new MutationObserver(mutations => {
      let run = false;
      for(let m of mutations){
          if(m.addedNodes.length > 0){ run = true; break; }
      }
      if(run) {
          observer.disconnect();
          retranslate();
          observer.observe(document.body, {childList: true, subtree: true});
      }
  });
  observer.observe(document.body, {childList: true, subtree: true});
});
